package com.example.demo.model;

public class Vartiste {
	
	private int id;

	private String nom;
	
	private float tarif;
	
	private int duree;
	
	private String img;
	
	

	public int getId() {
		return id;
	}



	public void setId(int id) {
		this.id = id;
	}



	public String getNom() {
		return nom;
	}



	public void setNom(String nom) {
		this.nom = nom;
	}



	public float getTarif() {
		return tarif;
	}



	public void setTarif(float tarif) {
		this.tarif = tarif;
	}



	public int getDuree() {
		return duree;
	}



	public void setDuree(int duree) {
		this.duree = duree;
	}



	public String getImg() {
		return img;
	}



	public void setImg(String img) {
		this.img = img;
	}



	public Vartiste() {
		super();
	}
	
	
}
